def randomize_board(num):
    # Make a deep copy of the original boards
    new_board = copy.deepcopy(boards)

    # Get the indices where we can replace the value from 1 to 10
    replace_indices = [(row, col) for row in range(len(new_board)) for col in range(len(new_board[0])) if new_board[row][col] == 1]

    # Ensure that num is not greater than the number of available locations to replace
    num = min(num, len(replace_indices))

    # Randomly choose num locations and replace the value from 1 to 10
    selected_indices = random.sample(replace_indices, num)
    for row, col in selected_indices:
        new_board[row][col] = 10

    return new_board

